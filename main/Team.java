package main;

public class Team {
	public String name;
	public int number;
	public String city, state, country;
	
	public Team(String name, int number, String city, String state, String country) {
		this.name = name;
		this.number = number;
		this.city = city;
		this.state = state;
		this.country = country;
	}
}
